
$(".my-rating-1").starRating({
    initialRating: 3.5,
    disableAfterRate: false,
    onHover: function (currentIndex, currentRating, $el) {
        $('.live-rating1').text(currentIndex);
    },
    onLeave: function (currentIndex, currentRating, $el) {
        $('.live-rating1').text(currentRating);
    }
});
